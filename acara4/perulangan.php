<?php
// Mulai loop dengan $i yang dimulai dari 100.
// Loop akan terus berjalan sampai $i mencapai 1000.
// Setiap kali loop berjalan, $i akan bertambah 1.
// Menampilkan nilai $i dan teks "i ke - " setiap iterasi, lalu berpindah ke baris berikutnya.
for ($i = 100; $i <= 1000; $i++) 
{
    // Cetak nilai $i yang sedang diproses, diikuti dengan teks "i ke - ".
    // \n menambahkan baris baru setelah setiap angka.
    //Menampilkan nilai $i dan teks "i ke - " setiap iterasi, lalu berpindah ke baris berikutnya.
    echo "i ke - $i \n";
}
