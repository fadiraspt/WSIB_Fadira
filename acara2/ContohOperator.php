<?php
// Mendefinisikan variabel $angka1 dengan nilai 10
$angka1 = 10;

// Mendefinisikan variabel $angka2 dengan nilai 2
$angka2 = 2;

// Melakukan pembagian antara $angka1 dan $angka2 (10 / 2)
// Hasilnya yaitu 5, dan hasil tersebut akan di tampilkan
//Bagian pertama dari kode menggunakan operator pembagian (/), dan hasilnya adalah 5 karena 10 dibagi 2 sama dengan 5
echo $angka1 / $angka2; // Output: 5


// Mendefinisikan variabel $angka3 dengan nilai 7
$angka3 = 7;

// Mendefinisikan variabel $angka4 dengan nilai 2
$angka4 = 2;

// Menghitung sisa hasil bagi (modulus) dari $angka3 dibagi $angka4 (7 % 2)
//Bagian kedua menggunakan operator modulus (%), yang menghasilkan sisa pembagian antara 7 dan 2, yaitu 1
// Hasilnya adalah 1, yang berarti sisa bagi dari 7 dibagi 2 adalah 1
echo $angka3 % $angka4; // Output: 1
?>
