<?php 
// Mendeklarasikan variabel $x dan $y dengan nilai masing-masing
$x = 10;  // Nilai variabel $x adalah 10
$y = 6;   // Nilai variabel $y adalah 6

// Melakukan penjumlahan antara $x dan $y
echo ($x + $y);  // Output: 16 (10 + 6)

// Melakukan pengurangan antara $x dan $y
echo ($x - $y);  // Output: 4 (10 - 6)

// Melakukan perkalian antara $x dan $y
echo ($x * $y);  // Output: 60 (10 * 6)

// Melakukan pembagian antara $x dan $y
echo ($x / $y);  // Output: 1.6667 (10 / 6)

// Menghitung sisa hasil bagi (modulus) antara $x dan $y
echo ($x % $y);  // Output: 4 (sisa pembagian 10 % 6)

// Mendeklarasikan variabel $a dengan string "Hello"
$a = "Hello";  

// Menggabungkan string $a dengan " world!" menggunakan operator penggabungan string (.)
//Operator penggabungan string (.) digunakan untuk menggabungkan teks. Misalnya, $a . " world!" menghasilkan "Hello world!"
$b = $a . " world!";  
echo $b;  // Output: "Hello world!"

// Menampilkan gabungan dari variabel $a dan $b beserta string tambahan
Echo $a . "--" . $b . " ini string operator ";  
// Output: "Hello--Hello world! ini string operator"
?>
