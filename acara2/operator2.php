<?php 
// Mendeklarasikan variabel $x dan $y dengan nilai masing-masing
$x = 10;  // Nilai variabel $x yaitu 10
$y = 6;   // Nilai variabel $y yaitu 6

// Melakukan operasi penjumlahan antara $x dan $y
echo ($x + $y); 

// Melakukan operasi pengurangan antara $x dan $y
echo ($x - $y);  

// Melakukan operasi perkalian antara $x dan $y
echo ($x * $y); 

// Melakukan operasi pembagian antara $x dan $y
echo ($x / $y);  

// Menghitung sisa hasil dari bagi (modulus) antara $x dan $y
echo ($x % $y);  

// Mendeklarasikan variabel $a dengan string "Hello"
$a = "Hello";  

// Menggabungkan string $a dengan " world!" menggunakan operator penggabungan string (.)
//Operator penggabungan string (.) digunakan untuk menggabungkan teks. contoh, $a . " world!" menghasilkan "Hello world!"
$b = $a . " world!";  
echo $b;  // Output: "Hello world!"

// Menampilkan gabungan dari variabel $a dan $b beserta string tambahan
Echo $a . "--" . $b . " ini string operator ";  
// Output: "Hello--Hello world! ini string operator"
?>
